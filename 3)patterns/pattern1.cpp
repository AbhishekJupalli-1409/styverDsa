#include<bits/stdc++.h>
using namespace std;
void pattern1(int t) {
    int  n=t;
    for (int i = 0;i < n;i++) {
        for (int j = 0;j < n;j++) {
            cout << "*";
        }
        cout << endl;

    }
}

int main() {
    int t;
    cin >> t;
    pattern1(t);
}

